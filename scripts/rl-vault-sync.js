#!/usr/bin/env node
/**
 * RL Reference Vault Sync
 * Copies the doc subset defined in docs/rewrite-labs/rl-vault-manifest.json
 * from the local rewrite-mcp clone into C:\dev\rl-ref, then generates
 * rl-ref/index.md from the manifest sections.
 *
 * Usage:
 *   node scripts/rl-vault-sync.js [--pull] [--dry-run]
 *     --pull     git pull the rewrite-mcp clone before syncing
 *     --dry-run  report what would sync without writing
 */

import { execSync } from "child_process";
import { cpSync, existsSync, mkdirSync, readFileSync, writeFileSync } from "fs";
import { dirname, join, resolve } from "path";
import { fileURLToPath } from "url";

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const manifestPath = join(repoRoot, "docs", "rewrite-labs", "rl-vault-manifest.json");
const args = process.argv.slice(2);
const doPull = args.includes("--pull");
const dryRun = args.includes("--dry-run");

const manifest = JSON.parse(readFileSync(manifestPath, "utf8"));
const clone = manifest.localClone;
const target = manifest.target;

if (!existsSync(clone)) {
  console.error(`[rl-vault-sync] local clone not found: ${clone}`);
  process.exit(1);
}

if (doPull) {
  console.log(`[rl-vault-sync] git pull in ${clone} ...`);
  execSync("git pull --ff-only", { cwd: clone, stdio: "inherit" });
}

const results = [];
let missing = 0;

for (const section of manifest.sections) {
  for (const rel of section.include) {
    const src = join(clone, rel);
    const dest = join(target, rel);
    if (!existsSync(src)) {
      results.push({ section: section.name, rel, status: "MISSING" });
      missing++;
      continue;
    }
    if (!dryRun) {
      mkdirSync(dirname(dest), { recursive: true });
      cpSync(src, dest);
    }
    results.push({ section: section.name, rel, status: dryRun ? "would-sync" : "synced" });
  }
}

const stamp = new Date().toISOString();
const lines = [
  "# RL Reference Vault Index",
  "",
  `Source: \`${manifest.source}\` (local clone: \`${clone}\`)`,
  `Manifest: \`docs/rewrite-labs/rl-vault-manifest.json\` (v${manifest.version})`,
  `Last sync: ${stamp}`,
  "",
  "> Generated by `node scripts/rl-vault-sync.js` — do not edit by hand.",
  "",
];

for (const section of manifest.sections) {
  lines.push(`## ${section.name}`, "");
  for (const rel of section.include) {
    const r = results.find((x) => x.rel === rel);
    const marker = r.status === "MISSING" ? " ⚠️ missing at source" : "";
    lines.push(`- \`${rel}\`${marker}`);
  }
  lines.push("");
}

if (!dryRun) {
  mkdirSync(target, { recursive: true });
  writeFileSync(join(target, "index.md"), lines.join("\n"), "utf8");
}

for (const r of results) {
  console.log(`${r.status.padEnd(10)} ${r.rel}`);
}
console.log(
  `[rl-vault-sync] ${results.length - missing}/${results.length} files ${dryRun ? "would sync" : "synced"} → ${target}${missing ? ` (${missing} MISSING — fix manifest)` : ""}`
);
process.exit(missing ? 2 : 0);
