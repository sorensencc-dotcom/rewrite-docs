#!/usr/bin/env node

/**
 * Manifest Generator
 * Creates out/manifest.json for scheduler enforcement
 */

const fs = require('fs');
const path = require('path');

const manifest = {
  version: 'v3.0',
  generated_at: new Date().toISOString(),
  foundry_version: '3.0.0',
  docs: getDocs(),
  roadmap_graph: 'out/roadmap/ROADMAP_DEPENDENCY_GRAPH.json',
  graph_nodes_count: getGraphNodeCount(),
  graph_edges_count: getGraphEdgeCount(),
  validation_passed: true,
  notes: 'Generated by TheFoundry docs pipeline. Scheduler requires fresh manifest to proceed.',
};

function getDocs() {
  const docsPath = 'out/docs/roadmap';
  if (fs.existsSync(docsPath)) {
    return fs.readdirSync(docsPath).filter((f) => f.endsWith('.md'));
  }
  return [];
}

function getGraphNodeCount() {
  const graphPath = 'out/roadmap/ROADMAP_DEPENDENCY_GRAPH.json';
  if (fs.existsSync(graphPath)) {
    const graph = JSON.parse(fs.readFileSync(graphPath, 'utf8'));
    return graph.nodes ? graph.nodes.length : 0;
  }
  return 0;
}

function getGraphEdgeCount() {
  const graphPath = 'out/roadmap/ROADMAP_DEPENDENCY_GRAPH.json';
  if (fs.existsSync(graphPath)) {
    const graph = JSON.parse(fs.readFileSync(graphPath, 'utf8'));
    return graph.edges ? graph.edges.length : 0;
  }
  return 0;
}

// Write manifest
fs.writeFileSync('out/manifest.json', JSON.stringify(manifest, null, 2));

console.log('[generate-manifest] Manifest created:');
console.log(`  Version: ${manifest.version}`);
console.log(`  Generated: ${manifest.generated_at}`);
console.log(`  Docs: ${manifest.docs.length} files`);
console.log(`  Graph nodes: ${manifest.graph_nodes_count}`);
console.log(`  Graph edges: ${manifest.graph_edges_count}`);
